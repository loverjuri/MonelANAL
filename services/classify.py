"""Auto-classification of expenses by comment keywords."""

KEYWORD_MAP = {
    "кофе": ("Еда", "Кафе"), "кафе": ("Еда", "Кафе"), "ресторан": ("Еда", "Рестораны"),
    "доставка еды": ("Еда", "Доставка"), "продукты": ("Еда", "Продукты"), "магазин": ("Еда", "Продукты"),
    "пятёрочка": ("Еда", "Продукты"), "перекрёсток": ("Еда", "Продукты"),
    "такси": ("Транспорт", "Такси"), "метро": ("Транспорт", "Метро"), "бензин": ("Транспорт", "Бензин"),
    "автобус": ("Транспорт", "ОТ"),
    "аренда": ("Жильё", "Аренда"), "жкх": ("Жильё", "ЖКХ"), "коммуналка": ("Жильё", "ЖКХ"),
    "аптека": ("Здоровье", "Аптека"), "врач": ("Здоровье", "Врач"), "стоматолог": ("Здоровье", "Врач"),
    "кино": ("Развлечения", "Кино"), "игра": ("Развлечения", "Игры"), "netflix": ("Развлечения", "Подписки"),
    "spotify": ("Развлечения", "Подписки"), "подписка": ("Развлечения", "Подписки"),
    "одежда": ("Прочее", "Одежда"), "обувь": ("Прочее", "Одежда"),
}


def auto_classify(comment: str) -> tuple[str, str] | None:
    """Returns (category, subcategory) or None if no match."""
    if not comment:
        return None
    low = comment.lower().strip()
    for keyword, cat_sub in KEYWORD_MAP.items():
        if keyword in low:
            return cat_sub
    return None
